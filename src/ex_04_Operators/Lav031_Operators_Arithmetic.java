package ex_04_Operators;

public class Lav031_Operators_Arithmetic {
    public static void main(String[] args) {
        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println("a+b");
        System.out.println("a-b");
        System.out.println("a*b");
        System.out.println("a/b");
        System.out.println("a/c");// because of double quotes it will give same answer

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);


    }
}
