package ex_10_Switch_Case;

public class Lab091_Interview {
    public static void main(String[] args) {
        int a = 11;
        switch (-1)
        {
            default:
                System.out.println("default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 9:
                System.out.println("9");
                break;
        }
    }
}
